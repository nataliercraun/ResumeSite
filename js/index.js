function about() {

	var t_margin = document.getElementById('link_row').style.marginTop; 

	if (t_margin == '30%') {
		document.getElementById('link_row').style.marginTop = '2%'; 
	} else {
		document.getElementById('link_row').style.marginTop = '30%';
	}
}

